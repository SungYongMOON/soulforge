import { existsSync, mkdtempSync, readdirSync, readFileSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import process from "node:process";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(scriptDir, "../..");
const packagesRoot = path.resolve(workspaceRoot, "packages");
const packDir = mkdtempSync(path.join(tmpdir(), "soulforge-theme-pack-"));

function loadThemePackages() {
  return readdirSync(packagesRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && entry.name.startsWith("theme-") && entry.name !== "theme-contract")
    .map((entry) => {
      const packageDir = path.resolve(packagesRoot, entry.name);
      const packageJson = JSON.parse(readFileSync(path.resolve(packageDir, "package.json"), "utf-8"));
      return {
        dirName: entry.name,
        packageName: packageJson.name ?? entry.name
      };
    })
    .sort((left, right) => left.packageName.localeCompare(right.packageName));
}

try {
  const themePackages = loadThemePackages();
  if (themePackages.length === 0) {
    console.error("FAIL theme package smoke test");
    console.error("  no theme-* workspace packages were found");
    process.exit(1);
  }

  for (const themePackage of themePackages) {
    const result = spawnSync(
      "npm",
      ["pack", "--workspace", themePackage.packageName, "--pack-destination", packDir],
      {
        cwd: workspaceRoot,
        stdio: "inherit",
        env: process.env
      }
    );

    if (result.status !== 0) {
      process.exit(result.status ?? 1);
    }

    const tarballPrefix = `${themePackage.packageName.replace("@", "").replaceAll("/", "-")}-`;
    const tarballs = readdirSync(packDir).filter(
      (fileName) => fileName.endsWith(".tgz") && fileName.startsWith(tarballPrefix)
    );

    if (tarballs.length === 0) {
      console.error("FAIL theme package smoke test");
      console.error(`  no tarball was generated by npm pack for ${themePackage.packageName}`);
      process.exit(1);
    }

    const tarballPath = path.join(packDir, tarballs[tarballs.length - 1]);
    if (!existsSync(tarballPath)) {
      console.error("FAIL theme package smoke test");
      console.error(`  generated tarball is missing for ${themePackage.packageName}: ${path.basename(tarballPath)}`);
      process.exit(1);
    }

    console.log(`PASS theme package smoke test (${themePackage.packageName} -> ${path.basename(tarballPath)})`);
  }
} finally {
  rmSync(packDir, { recursive: true, force: true });
}
