import { existsSync, mkdtempSync, readdirSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import process from "node:process";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(scriptDir, "../..");
const packDir = mkdtempSync(path.join(tmpdir(), "soulforge-theme-pack-"));

try {
  const result = spawnSync(
    "npm",
    ["pack", "--workspace", "@soulforge/theme-adventurers-desk", "--pack-destination", packDir],
    {
      cwd: workspaceRoot,
      stdio: "inherit",
      env: process.env
    }
  );

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }

  const tarballs = readdirSync(packDir).filter((fileName) => fileName.endsWith(".tgz"));
  if (tarballs.length === 0) {
    console.error("FAIL theme package smoke test");
    console.error("  no tarball was generated by npm pack");
    process.exit(1);
  }

  const tarballPath = path.join(packDir, tarballs[0]);
  if (!existsSync(tarballPath)) {
    console.error("FAIL theme package smoke test");
    console.error(`  generated tarball is missing: ${tarballs[0]}`);
    process.exit(1);
  }

  console.log(`PASS theme package smoke test (${tarballs[0]})`);
} finally {
  rmSync(packDir, { recursive: true, force: true });
}
